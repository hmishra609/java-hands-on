package org.example.collections.set;

import java.util.*;

public class HashSetExample {
    public static void main(String[] args) {
        // 1. HashSet (The "Jumbled" Set)
        Set<String> hash = new HashSet<>();
        hash.add("Z"); hash.add("A"); hash.add("B"); hash.add("X");
        System.out.println("HashSet (Unpredictable): " + hash); 


    }
}